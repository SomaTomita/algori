# ------------------------------------------------------------
# ナイーブ解 (比較用)
# ------------------------------------------------------------
def max_subarray_length_k_naive(nums: List[int], k: int) -> int:
    """全ての長さ k の窓を毎回足し直す: O(n * k)"""
    largest = 0
    for i in range(len(nums) - k + 1):
        largest = max(largest, sum(nums[i : i + k]))  # ここで毎回 k 個足している
    return largest


"""
========================================================
流れ (ナイーブ版): nums = [2, 1, 5, 1, 3, 2], k = 3
========================================================

ループ回数 = range(len(nums) - k + 1) = range(6 - 3 + 1) = range(4)
    → i は 0, 1, 2, 3 の 4 回 (長さ k の窓は n - k + 1 = 4 個置ける)
    → i は「窓の左端」。各 i で nums[i:i+k] を切り出し、毎回 k 個を足し直す。

--------------------------------------------------------

i = 0:  nums[0:3] = [2, 1, 5]   sum = 2 + 1 + 5 = 8
        largest = max(0, 8) = 8

    index:  0  1  2  3  4  5
          [ 2  1  5] 1  3  2
            ↑─────↑
            sum = 8   ← largest = 8

--------------------------------------------------------

i = 1:  nums[1:4] = [1, 5, 1]   sum = 1 + 5 + 1 = 7
        largest = max(8, 7) = 8

    index:  0  1  2  3  4  5
            2 [1  5  1] 3  2
               ↑─────↑
               sum = 7   (largest はまだ 8)

--------------------------------------------------------

i = 2:  nums[2:5] = [5, 1, 3]   sum = 5 + 1 + 3 = 9
        largest = max(8, 9) = 9   ← 更新!

    index:  0  1  2  3  4  5
            2  1 [5  1  3] 2
                  ↑─────↑
                  sum = 9   (largest = 9)

--------------------------------------------------------

i = 3:  nums[3:6] = [1, 3, 2]   sum = 1 + 3 + 2 = 6
        largest = max(9, 6) = 9

    index:  0  1  2  3  4  5
            2  1  5 [1  3  2]
                     ↑─────↑
                     sum = 6   (largest = 9)

--------------------------------------------------------

ループ終了 → return 9 ✓

なぜ range(len(nums) - k + 1) なのか:
    窓の左端として有効な最後の位置は len(nums) - k = 3。
    そこから k 個取ると nums[3:6] でちょうど末尾 (index 5) に届く。
    range は上限を含まないので +1 して i=3 まで回す。
      - +1 を忘れる (range(3)) → 最後の窓 [1, 3, 2] を取りこぼす
      - 大きすぎる (range(6)) → i=4,5 で長さ k 未満の窓まで足してバグ

Sliding Window 版との違い:
    回る窓は同じ 4 つ。ナイーブは毎回 sum() で k 個足し直す → O(n * k)。
    Sliding Window は「1 個抜いて 1 個足す」差分更新 → O(n)。
"""


