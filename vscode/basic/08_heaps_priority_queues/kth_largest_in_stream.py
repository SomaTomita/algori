# ------------------------------------------------------------
r"""
min heap = 「親 ≤ 子」の木。配列 [a,b,c,d,..] は上から a が根、その子が b,c …。
根 heap[0] = 木の一番上 = 全要素の最小。これを使うのがこの解法のキモ。

================================================================
1. なぜ pop が要るのか  (ここが一番大事)
================================================================
heap[0] (= 根 = 一番チビ) が「k 番目に大きい値」になるのは、
heap がちょうど k 個のときだけ。

  ● ちょうど k 個 = 3個 {4,5,8}         ● pop せず全部ためた 5個 {2,3,4,5,8}
                                              (= ストリーム 8,5,4,3,2 を全部)
        (4)                                       (2)
        / \                                       / \
       5   8                                     3   4
                                                / \
                                               5   8
    heap[0] = 4                               heap[0] = 2
    = 3個の最小                               = 全体の最小
    = 大きい順で 3番目                        = 大きい順で 5番目(最下位)
    = 答え ✓                                  = 答えじゃない ✗

  → pop しないと箱が膨らみ、heap[0] が「全体の最下位」に化ける。
  → だから はみ出たら最小を1個捨てて 箱を常に k 個に保つ。
     そうすれば heap[0] を見るだけで k 番目が取れる。これが pop の目的。

  捨てて安全な理由:
     捨てるのは常に「その時の上位 k 個に入れなかった一番チビ」。
     ストリームは追加だけ(削除なし)なので k 番目のラインは上がる一方。
     一度ラインから落ちた値が後で k 番目に復活することはない → 捨ててOK。

================================================================
2. トレース A: 空から育てる  k=3, nums=[]
================================================================
size が k に届くまでは「捨てるものが無い」→ pop しない。
k を超えた瞬間だけ pop する。木で見ると:

  add(5) push→size1     add(2) push→size2     add(8) push→size3
     (5)                   (2)                   (2)
                           /                     / \
                          5                     5   8
   size1≤3 捨てない       size2≤3 捨てない       size3≤3 捨てない
   heap[0]=5              heap[0]=2             heap[0]=2

  add(1): push 1 → size4 → はみ出た → 最小(根=1)を pop
     (1)                      (2)
     / \        pop 1         / \
    2   8       ────→        5   8     heap[0]=2
   /                                   (入れた 1 がそのまま捨てられた)
  5

  add(9): push 9 → size4 → はみ出た → 最小(根=2)を pop
     (2)                      (5)
     / \        pop 2         / \
    5   8       ────→        9   8     heap[0]=5
   /                                   (前からいた最小 2 が捨てられた)
  9

  ┌──────────┬────────────┬───────────┬───────────┬─────────┐
  │ ステップ │ push後size │ size > k? │ pop する? │ heap[0] │
  ├──────────┼────────────┼───────────┼───────────┼─────────┤
  │ add(5)   │ 1          │ No        │ 捨てない  │ 5       │
  │ add(2)   │ 2          │ No        │ 捨てない  │ 2       │
  │ add(8)   │ 3          │ No        │ 捨てない  │ 2       │
  │ add(1)   │ 4          │ Yes       │ 1を捨てる │ 2       │
  │ add(9)   │ 4          │ Yes       │ 2を捨てる │ 5       │
  └──────────┴────────────┴───────────┴───────────┴─────────┘
  ・size ≤ k (上3行) = まだ k 個貯まってない → 捨てるものが無い → pop しない
       ↑ これが add の `if len(heap) > k:` が偽になるケース
  ・size > k (下2行) = 1個はみ出した → 根(最小)を1個 pop して k 個に戻す

================================================================
3. トレース B: 初期配列あり (本番例)  k=3, nums=[4,5,8,2]
================================================================
右側は「これまでの全要素を大きい順に並べた列」。その 3番目を [ ] で囲った。
それが答え (= ナイーブの sorted(全部)[-k]) で、heap[0] と必ず一致する。

  初期化 KthLargest(3, [4,5,8,2]):
    heapify → {2,4,5,8} (4個) → size4>3 なので最小 2 を pop → {4,5,8}
        大きい順:  8  5 [4]  2          heap[0]=4  ✓  (2 は 4番目でライン外)

  add(3):  push 3 → {3,4,5,8} → 最小 3 を pop → {4,5,8}
        大きい順:  8  5 [4]  3  2          heap[0]=4  ✓
  add(5):  push 5 → {4,5,5,8} → 最小 4 を pop → {5,5,8}
        大きい順:  8 [5]  5  4  3  2        heap[0]=5  ✓
  add(10): push 10 → {5,5,8,10} → 最小 5 を pop → {5,8,10}
        大きい順: 10  8 [5]  5  4  3  2     heap[0]=5  ✓
  add(9):  push 9 → {5,8,9,10} → 最小 5 を pop → {8,9,10}
        大きい順: 10  9 [8]  5  5  4  3  2  heap[0]=8  ✓
  add(4):  push 4 → {4,8,9,10} → 最小 4 を pop → {8,9,10}
        大きい順: 10  9 [8]  5  5  4  4  3  2  heap[0]=8 ✓

  毎回「入れて、はみ出た最小を1個捨てて、残った最小(heap[0])を返す」だけ。

================================================================
4. __init__ は while・add は if、の理由
================================================================
違いは「1回の処理で何個 push するか」だけ。

  ┌──────────┬───────────────────────┬──────────────┬──────┐
  │          │ 1回で push する数     │ はみ出る数   │ 構文 │
  ├──────────┼───────────────────────┼──────────────┼──────┤
  │ __init__ │ 初期配列ぜんぶ (不定) │ 2個以上あり  │ while│
  │ add      │ 1個だけ               │ 多くて1個    │ if   │
  └──────────┴───────────────────────┴──────────────┴──────┘
  ・__init__: 例 k=1, nums=[5,3,8,1] は size4。1回 pop では 3個残る。
              size が 1 になるまで 3回 pop が要る → ループ(while)。
  ・add:      size=k に 1個足すだけ → はみ出ても高々1個 → 1回 pop で戻る → if。

heap が持つのは常に約 k 個だけなので、add は push+pop の O(log k) で済む
(全部を毎回ソートするナイーブの O(m log m) より速い)。
"""


# ------------------------------------------------------------
