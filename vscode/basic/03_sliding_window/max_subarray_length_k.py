"""
Maximum Subarray of Length K  (固定サイズ Sliding Window)

問題:
    非負整数の配列 nums と整数 k が与えられる。
    長さがちょうど k の連続部分配列 (subarray) の和の最大値を返せ。

例:
    nums = [2, 1, 5, 1, 3, 2], k = 3  →  9   (= 5 + 1 + 3)

ナイーブ:
    全ての長さ k の窓を毎回足し直す → O(n * k)

Sliding Window:
    1 つ進めるごとに「左端を抜き、右端を足す」だけ → O(n)
"""

from typing import List


# ------------------------------------------------------------
# 解法: 固定サイズ Sliding Window
# ------------------------------------------------------------
def max_subarray_length_k(nums: List[int], k: int) -> int:
    # 1. 最初の窓 [0..k-1] の和を作る
    window_sum = sum(nums[:k])
    largest = window_sum

    # 例: nums=[2,1,5,1,3,2], k=3 のとき
    #   初期: window_sum = 2+1+5 = 8, largest=8
    #   r=3: l=0, window_sum = 8 - 2 + 1 = 7, largest=8
    #   r=4: l=1, window_sum = 7 - 1 + 3 = 9, largest=9 ← 更新
    #   r=5: l=2, window_sum = 9 - 5 + 2 = 6, largest=9

    # 2. r を 1 つずつ右にスライド。l = r - k で左端が決まる
    for r in range(k, len(nums)):
        l = r - k
        window_sum -= nums[l]  # 左端を抜く
        window_sum += nums[r]  # 右端を足す
        largest = max(largest, window_sum)

    return largest


# ------------------------------------------------------------
# 動作確認
# ------------------------------------------------------------
if __name__ == "__main__":
    nums = [2, 1, 5, 1, 3, 2]
    k = 3
    print(max_subarray_length_k(nums, k))  # → 出力: 9   (5+1+3 が最大)


