"""
ナイーブ (毎回 sort):
- __init__ : O(n)            リストのコピーだけ
- add      : O(m log m)      m = その時点の全要素数。毎回フルソート
- 累積で見ると add を呼ぶほど重くなる (ストリーム長に比例して悪化)

heap 解 (size-k min heap):
- __init__ : O(n + (n-k) log n)   heapify O(n) + 余分を pop で落とす
             ≈ 最悪 O(n log n) (k が 1 のように小さいと n-k 回 pop)
- add      : O(log k)        push + (はみ出たら) pop。heap の size は常に約 k
- heap[0]  : O(1)            root を見るだけ
- Space    : O(k)            上位 k 個しか持たない

ポイント:
- add が O(log k) なのが効く。ナイーブの O(m log m) と比べ、
  ストリームが長くても add のコストが k だけで決まる (m に依存しない)。
- k が小さく n が巨大な streaming 用途で特に有利。これが #703 が
  「配列(#215)」ではなく「ストリーム」かつ「クラス設計」になっている理由。
"""
