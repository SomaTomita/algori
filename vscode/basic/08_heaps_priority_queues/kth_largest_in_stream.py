"""
Kth Largest Element in a Stream (LeetCode #703)  Easy

クラスを設計する問題。数の「ストリーム」(後からどんどん数が増えていく) に対して、
add() を呼ぶたびに「その時点での k 番目に大きい値」を返し続ける。

#215 (Kth Largest Element in an Array) との違い:
    #215: 配列は固定。k 番目を 1 回求めるだけ。
    #703: 配列は増えていく。add() のたびに k 番目を返す (= 設計問題)。

実装する 2 つの関数:
    KthLargest(k, nums) : k と初期配列 nums を受け取る (nums は k 個未満のこともある)
    add(val) -> int     : val を追加し、追加後の k 番目に大きい値を返す
                          (add が呼ばれる時点では必ず k 個以上あると保証される)

注意: 「k 番目に大きい」は重複を含むソート順での k 番目 (distinct ではない)。
    例: [3,2,1,1], k=3 → 大きい順 3,2,1,1 の 3 番目 = 1

例: k=3, nums=[4,5,8,2]
    add(3)  → 4      (大きい順 8,5,4,3,2 の 3 番目 = 4)
    add(5)  → 5      (8,5,5,4,3,2 → 3 番目 = 5)
    add(10) → 5      (10,8,5,5,4,3,2 → 3 番目 = 5)
    add(9)  → 8      (10,9,8,5,5,4,3,2 → 3 番目 = 8)
    add(4)  → 8      (10,9,8,5,5,4,4,3,2 → 3 番目 = 8)
"""

import heapq
from typing import List


# ------------------------------------------------------------
# ナイーブ: 全部リストに貯めて、add のたびに sort して [-k]
# ------------------------------------------------------------
# 一番素直。これまでの数を全部持っておき、add で末尾に足してから
# 毎回ソートして後ろから k 番目を返す。#215 のナイーブと同じ発想。
# 短所: add のたびに全体を sort するので 1 回が O(n log n)。
#       ストリームが長くなるほど add がどんどん重くなる。
class KthLargestNaive:
    def __init__(self, k: int, nums: List[int]):
        self.k = k
        self.nums = list(nums)  # これまでに来た全ての数

    def add(self, val: int) -> int:
        self.nums.append(val)  # 末尾に追加 O(1)
        # 昇順の末尾から k 番目 = k 番目に大きい値 (下の補足参照)
        return sorted(self.nums)[-self.k]  # 毎回ソート O(n log n)


# 補足: なぜ [-self.k] が「k 番目に大きい値」なのか
# ------------------------------------------------------------
# sorted() は昇順(小さい順)に並べたリストを返す。
# Python の負の index は末尾から数えるので、
#
#     sorted([4,5,8,2,3])  =  [ 2,  3,  4,  5,  8 ]
#                 index →      -5  -4  -3  -2  -1
#
#     [-1] = 8 = 1 番目に大きい (最大)
#     [-2] = 5 = 2 番目に大きい
#     [-3] = 4 = 3 番目に大きい   ← k=3 ならこれ
#      :
#     [-k] =     k 番目に大きい
#
# つまり [-self.k] は「昇順の末尾から k 個目 = k 番目に大きい値」。
# sorted(reverse=True)[k-1] と書いても同じ。


# ------------------------------------------------------------
# 解法: サイズ k の min heap を維持する  → add が O(log k)  ★推奨
# ------------------------------------------------------------
# キーになる気づき:
#   (1) このストリームは「追加だけ」で削除が無い。
#       → いま上位 k 個に入れない小さい値は、今後も k 番目になれない。
#         (数が増えれば k 番目のラインは上がる一方なので)
#       → だから「上位 k 個」だけ持てば十分。それ以外は捨ててよい。
#   (2) 持っている k 個の中の「最小」が、まさに k 番目に大きい値。
#       → min heap なら heap[0] でその最小を O(1) で読める。これが答え。
#   (3) なぜ max heap でなく min heap か:
#       捨てたいのも答えとして読みたいのも「持っている k 個の中の最小」。
#       min heap はその最小が常に root(heap[0]) に来るので両方 O(1)/O(log k)。
class KthLargest:
    def __init__(self, k: int, nums: List[int]):
        self.k = k
        self.min_heap = list(nums)
        heapq.heapify(self.min_heap)  # O(n) で min heap 化
        # 初期配列が k 個より多ければ、小さい方から余分を捨てて size を k にする
        # (while な理由 / 捨ててよい理由は下のトレース参照)
        while len(self.min_heap) > k:
            heapq.heappop(self.min_heap)

    def add(self, val: int) -> int:
        heapq.heappush(self.min_heap, val)  # まず入れる O(log k)
        # size が k を超えたら最小を 1 個捨てて size を k に戻す
        # (初期が k 個未満だと size <= k のことがあるので、その時は捨てない)
        if len(self.min_heap) > self.k:
            heapq.heappop(self.min_heap)
        return self.min_heap[0]  # k 個の中の最小 = k 番目に大きい  O(1)


# ------------------------------------------------------------
# 動作確認 (ナイーブと heap 解が同じ結果を返すか照合)
# ------------------------------------------------------------
def _check(k: int, nums: List[int], adds: List[int], expected: List[int]) -> None:
    a = KthLargestNaive(k, nums)
    b = KthLargest(k, nums)
    ra = [a.add(v) for v in adds]
    rb = [b.add(v) for v in adds]
    ok = ra == rb == expected
    print(f"[{'OK' if ok else 'NG'}] k={k} nums={nums} adds={adds}")
    print(f"      naive={ra}  heap={rb}  expected={expected}")


if __name__ == "__main__":
    # LeetCode の例
    _check(3, [4, 5, 8, 2], [3, 5, 10, 9, 4], [4, 5, 5, 8, 8])
    # 初期配列が k 個未満のケース (constructor 時点では k 個無くてよい)
    _check(1, [], [-3, -2, -4, 0, 4], [-3, -2, -2, 0, 4])
    # k=1 は常に「これまでの最大」を返す
    _check(1, [10], [5, 11, 9, 20], [10, 11, 11, 20])


